    <h2>Dinner</h2>
    <h3>Lebanese-Style Lentil Soup</h3>
        <p>Ingredients: 
                <ul>
                    <li>3 cups red lentils</li>
                    <li>12 cups water</li>
                    <li>1 large red onion, diced</li>
                    <li>2 small Yukon gold potatoes, diced</li>
                    <li>1 medium zucchini, diced</li>
                    <li>4 cups Swiss chard, chopped</li>
                    <li>1 bunch of cilantro, chopped</li>
                    <li>1/4 cup of lemon juice, more to taste</li>
                    <li>1 teaspoon cumin</li>
                    <li>Salt and pepper, to taste</li>
                </ul>
                In a large pot, add lentils and water and place on medium heat to boil. In a pan, sauté onions until pink, about 5 minutes. Add chopped Swiss chard and sauté together until chard is wilted, 2-3 minutes. After lentils have cooked about 8 minutes, add onion chard mixture, along with diced potatoes and zucchini to the boiling water. Add salt and pepper to taste. Reduce heat to medium-low, cover the pot, and cook until the potatoes and lentils are completely cooked, about 12 minutes. Add the chopped cilantro. Stir and cook for about 5 minutes. Add lemon juice and cumin before serving.
