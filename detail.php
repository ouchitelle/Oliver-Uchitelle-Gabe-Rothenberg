        <h2>Lunch</h2>
        <h3>Winter Potatoe Salad</h3>
        <p>Ingredients: 
                <ul>
                    <li>8 cups water</li>
                    <li>2 pounds patatoes, peeled and cut into quarters or eighths</li>
                    <li>1/2 cup chopped fresh dill</li>
                    <li>1/2 cup extra-virgin olive oil</li>
                    <li>3 to 5 tablespoons red wine vinegar</li>
                    <li>Salt and pepper to taste</li>
                    <li>2 cups arugula, chopped</li>
                    <li>2 cups spinach, chopped</li>
                    <li>1 large sweet onion(like Vidalia), thinly sliced</li>
                    <li>1 small head green leaf or romaine lettuce, chopped</li>
                    <li>1 small radish, sliced(optional, for garnihs)</li>
                </ul>
            Instructions: Bring 8 cups of water in saucepan to boil. Add potatoes and cook uncovered until tender, about 12 minutes. Test by piercing potatoes with a fork—if they pierce easily, they are ready. Drain potatoes and let cool. In a small bowl, combine dill, olive oil, and vinegar; season with salt and pepper to taste. Whisk until well combined. In a large serving bowl, combine potatoes with dressing and toss well. Just before serving, add remaining ingredients through lettuce and toss to combine. Garnish with radish, if using.
